package com.github.micli.cognitiveservice.api.face;

import java.util.List;
//
// This class is definitely huge. It's only used for face detection result.
// It's ugly. And I didn't find a easy way to implement it. All properties of FaceAttributes are not initialized at constructor,
// because of all members will be initialize by Alibaba fastJSON.
//
public final class FaceAttributes {

	private float age;
	private String gender;
	private float smile;
	private FacialHair facialHair;
	private String glasses;
	private HeadPos headPos;
	private Hair hair;
	private Makeup makeup;
	private Occlusion occlusion;
	private List<TypeValue> accessories;
	private Blur blur;
	private Exposure exposure;
	private Noise noise;
	
	public float getAge() {
		return age;
	}
	public void setAge(float age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public float getSmile() {
		return smile;
	}
	public void setSmile(float smile) {
		this.smile = smile;
	}
	public FacialHair getFacialHair() {
		return facialHair;
	}
	public void setFacialHair(FacialHair facialHair) {
		this.facialHair = facialHair;
	}
	public String getGlasses() {
		return glasses;
	}
	public void setGlasses(String glasses) {
		this.glasses = glasses;
	}
	public HeadPos getHeadPos() {
		return headPos;
	}
	public void setHeadPos(HeadPos headPos) {
		this.headPos = headPos;
	}
	public Hair getHair() {
		return hair;
	}
	public void setHair(Hair hair) {
		this.hair = hair;
	}
	public Makeup getMakeup() {
		return makeup;
	}
	public void setMakeup(Makeup makeup) {
		this.makeup = makeup;
	}
	public Occlusion getOcclusion() {
		return occlusion;
	}
	public void setOcclusion(Occlusion occlusion) {
		this.occlusion = occlusion;
	}
	public List<TypeValue> getAccessories() {
		return accessories;
	}
	public void setAccessories(List<TypeValue> accessories) {
		this.accessories = accessories;
	}
	public Blur getBlur() {
		return blur;
	}
	public void setBlur(Blur blur) {
		this.blur = blur;
	}
	public Exposure getExposure() {
		return exposure;
	}
	public void setExposure(Exposure exposure) {
		this.exposure = exposure;
	}
	public Noise getNoise() {
		return noise;
	}
	public void setNoise(Noise noise) {
		this.noise = noise;
	}
	
	public FaceAttributes() {
		
	}
}
